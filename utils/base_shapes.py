import numpy as np
from typing import Tuple

def mesh_bounding_box(vao_data):
    v, i = base_cube()
    
    pmin = vao_data.min(axis=0)[:3]
    pmax = vao_data.max(axis=0)[:3]
    
    S = v[:, :3]  # take the sign template (x,y,z) from v
    # Use tolerance to detect "negative" vs "positive"
    neg = S < -1 + 1e-6  # or use np.isclose(S, -1.0, atol=1e-6)
    # Broadcast pmin/pmax over rows
    P = np.where(neg, pmin, pmax)  # shape (N,3)
    v[:, :3] = P
    return v, i

